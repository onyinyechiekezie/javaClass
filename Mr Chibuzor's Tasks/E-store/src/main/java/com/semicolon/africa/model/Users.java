package main.java.com.semicolon.africa.model;

public class Users {
    private int age;
    private String email;
    private String homeAddress;
    private String name;
    private String password;
    private String phone;

    public Users(int age, String email, String homeAddress, String name, String password, String phone) {
        this.age = age;
        this.email = email;
        this.homeAddress = homeAddress;
        this.name = name;
        this.password = password;
        this.phone = phone;
    }
}

